<h2>Shortcode Examples</h2> 
<hr />
<h4>How to ignore scriptures in content</h4>
<i>Ignore Scriptures only works while the Auto Highlighting is Enabled</i>
<p>
	When using the Ignore Scriptures short when you want to turn off the 
	auto highlighting feature for a section of content.
</p>

<pre style="background: #ffffff; padding: 15px;">
This verse will auto highlight. John 3:16
[IgnoreScriptures]
This scripture will not highlight. John 3:16
[/IgnoreScriptures]
</pre>

<h4>How to highlight scriptures in content</h4>
<i>Highlight Scriptures only works while the Auto Highlighting is Disabled</i>
<p>
	Using the Highlight Scriptures shortcode you can manually highlight 
	scriptures in a section of content you intent to allow highlighting 
	to occur.
</p>

<pre style="background: #ffffff; padding: 15px;">
This scripture will not highlight. John 3:16
[HighlightScriptures]
This scripture will highlight John 3:16
[/HighlightScriptures]
</pre>